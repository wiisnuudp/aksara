package com.example.aksara;

public class KamusList {

    private String jawa, indonesia;

    public KamusList(String jawa, String indonesia){
        this.jawa = jawa;
        this.indonesia = indonesia;
    }

    public String getJawa() {
        return jawa;
    }

    public String getIndonesia() {
        return indonesia;
    }
}
